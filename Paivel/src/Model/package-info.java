package Model;

//Agendamento de eventos
/**
 * Uma taxa de 30% do valor total e' paga para confirmar a realizacao do evento,
 * caso contrario, a data ficara disponivel para qualquer outro cliente. Um
 * ponto importante, a Vila Verde nao possui um prazo de pagamento dos 30%,
 * logo, quem paga primeiro os 30% fica com o espaco e a data...
 *
 * A ultima parcela de pagamento devera ser efectuada 15 dias antes do evento. O
 * pagamento pode/nao ser efectuado por parcelas, e a cada pagamento, um recibo
 * de pagamento e' emitido.
 */

//Pagamento de Servicos e Outros
/**
 * O pagamento do espaco e' feito por pessoa, pelo preco de um preco unico pelo
 * tipo de salao (1300 pela tenda e 1200 pelo alpendre, eu acho que e' esse o
 * nome).
 *
 * O vila verde possui pacote de extras, que contem Djs(19.000 MT), Cantora da
 * casa(Marlene - 40.000MT), fotos e videos. Esses servicos nao sao pagos de
 * forma conjunta, ou seja, esses extras sao algo a parte porque os envolvidos
 * sao terceiros... cada servico extra pode vir isolado.
 *
 * Eles tambem possuem um bar (barman por sua conta), o dj e o som sao da sua
 * conta (se voce nao contratar o extra deles).
 *
 * Os saloes sao climatizados, mas isso tambem e' um extra se quiseres o ac
 * ligado (usam acs industriais)
 */

//Adiamento de Evento
/**
 * Um adiamento mediante uma justificacao plausivel e' permito sem a cobranca de
 * nenhuma taxa extra. Mas para os adiamentos subsequentes, e' cobrada uma taxa
 * (30.00MT na Vila Verde) sem sequer perguntar os motivos.
 *
 */

//Anti-Visao
/**
 * Apos o pagamento dos 30%, e' agendado um encontro entre o cliente e o
 * gestor/outro funcionario para que o cliente escolha elementos de decoracao,
 * como guardanapos, cadeiras e tipo de pratos.
 */

